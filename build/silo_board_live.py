#!/usr/bin/env python3
"""
Live ServiceTitan engine for the Silo Leaderboard.

Ranks silo (maintenance lead-gen) technicians by TGL sales, MTD and YTD, for
Sierra, Ultimate and Russett (plus a combined board). Metric definitions
mirror John's Silo Command Center (sierra-ropp-dashboard) where they exist
there, and the Hyperion tech-board everywhere else. Roster comes from
People > Technicians (active only), picked by team:

    sierra     team "1Silo"
    ultimate   team "1 - SILO Techs"
    russett    team "Service - Maintenance"   (no dedicated silo team; the
               maintenance crew is Russett's lead-gen equivalent)

Per tech, per calendar month (summed across months for YTD):

  tglSales       subtotal of estimates SOLD in the month whose job was
                 generated by this tech (job.jobGeneratedLeadSource.employeeId)
  tgls           TGL estimate visits created in the month (tenant-local
                 createdOn): jobs with jobGeneratedLeadSource.employeeId ==
                 tech whose job type name starts with "Estimate". The install
                 jobs booked from a sold TGL estimate carry the same lead
                 link and are deliberately NOT counted (they'd double-count
                 the lead) - this matches John's TGLs-created report.
  flips/nextDay  of those TGL jobs, ones whose first appointment falls on the
                 same tenant-local day as creation (flip) or the next day.
                 Flip rate = flips / tgls; the +next-day rate adds nextDay.
                 (John's SAMEDAY_DATA: scheduled date vs created date.)
  jobs / opps    completed jobs where the tech holds a payroll split > 0;
                 opportunity = !noCharge or total >= jobType soldThreshold
  tglRate        tgls / jobs (John's "conversion rate": TGLs per call ran)
  revenue        job.total of completed jobs, split by payroll job-split %
  memberships    membership line items on sold estimates of the tech's
                 completed jobs - renewals included (tech-board SKU sets)
  memb conv      non-member jobs where a membership was sold / non-member jobs
  memb offered   non-member jobs where ANY estimate on the job (sold or not)
                 carries a membership line item / non-member jobs

Closed months are cached in data/silo-board-history.json and recomputed at
most daily until they are 10 days past month-end, then frozen. The current
month is recomputed on every run.

CLI smoke test:
    py build/silo_board_live.py                 # all companies, current month
    py build/silo_board_live.py sierra 2026-06  # one company, one month
"""
