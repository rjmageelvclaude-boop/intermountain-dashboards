#!/usr/bin/env python3
"""
Live ServiceTitan engine for the CASPR (comfort report) board
(site/comfort-board/).

Replaces the monthly "MASTER NEXTSTAR COMFORT REPORT" workbook: per company
(Sierra / Ultimate / Russett), the current month's comfort-advisor sales
funnel by lead type plus the technician turnover funnel, daily and MTD.

Lead type of a sales-BU job (the workbook's four columns):
    costco     job sits in the Costco BU (Sierra only)
    selfgen    jobGeneratedLeadSource.employeeId is a roster Comfort Advisor
    turnover   jobGeneratedLeadSource.employeeId is anyone else (a tech lead;
               the linked source job tells COD vs Maintenance)
    marketed   no generated-lead source - the call came from marketing

Comfort Advisor metrics (daily by tenant-local day + MTD):
    leads ran  completed sales-BU jobs assigned to the CA, day of completion.
               Sold runs are auto-marked No Charge/Non-Opportunity the moment
               their project is created (the revenue moves to the install
               ticket), so a plain opportunity filter would drop exactly the
               sold calls: a run counts when it passes the opportunity rule
               OR anything was sold on it; No Charge/Non-Opp runs where
               nothing sold are bad leads and don't count
    sold       jobs where the CA has an active Sold estimate, day of soldOn
    revenue    subtotals of the CA's active Sold estimates, day of soldOn
               (ca-board's validated Sales definition, split by lead type)

Technician turnover funnel (the workbook's Technician Input sheet):
    opps       completed HVAC demand/maintenance-BU jobs assigned to the tech
               that are opportunities (call-board rule: !noCharge or
               total >= jobType soldThreshold), split COD vs Maint by BU
    ropps      completed service-BU jobs assigned to the tech carrying the
               ROPP tag, net of Management Removed ROPP (Sierra) - the
               board's turnover-rate denominator (TO rate = turns / ropps)
    turns      sales-BU jobs generated by the tech (jobGeneratedLeadSource),
               day of creation, COD/Maint by the SOURCE job's BU; canceled
               turnovers tracked separately for the cancellation rate
    ran        those turnover jobs that completed (same bad-lead exclusion
               as leads ran)
    sales      count + subtotal of active Sold estimates on those turnover
               jobs, day of soldOn

Monthly plan inputs (budgeted days, service calls, turnover %, close % and
average ticket per lead type) are entered on the board itself and live in the
shared Apps Script store (co="comfort") - the engine ships actuals only.

The month cache (data/comfort-board-cache-<TENANT>.json) backfills the
current month once, then syncs deltas via modifiedOnOrAfter; it resets on
month rollover.

CLI smoke test:
    py build/comfort_board_live.py sierra
"""
