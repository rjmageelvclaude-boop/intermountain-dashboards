def _history_days(tz, n=HISTORY_WEEKDAYS):
    """All days back through the last n weekdays before today (oldest first).

    Weekends inside that span are included (added 2026-07-20 so the date
    picker can show Saturday/Sunday); the frontend's trend arrows and WoW
    bars filter to weekdays themselves, so trends stay business-day based.
    """
    days, d, wk = [], local_today(tz), 0
    while wk < n:
        d -= dt.timedelta(days=1)
        days.append(d)
        if d.weekday() < 5:
            wk += 1
    return list(reversed(days))


def read_history():
    """History as currently cached on disk - no API calls (backfill runs separately)."""
    cache = _load_json(HISTORY_FILE, {})
    out = {}
    for company, co in COMPANIES.items():
        wanted = {d.isoformat() for d in _history_days(co["tz"])}
        entries = [e for e in cache.get(company, []) if e["date"] in wanted]
        out[company] = sorted(entries, key=lambda e: e["date"])
    return out


def compute_history(progress=None):
    """Past-day metrics per company (weekends included), cached on disk
    (past days never change).

    Entries missing sameDayDef=8 predate the current metric definitions (v2:
    sold-today same-day installs; v3: memberships sold from the memberships
    endpoint with the office/non-job split; v4: ROPP excludes Management
    Removed ROPP - all 2026-07-15; v5: TGL set requires the generating job
    to be Completed, same day; v6: daily revenue = invoices of jobs COMPLETED
    that local day, matching the ST dashboard's Completed Revenue; v7:
    membership conversion / options-per-opp also on the completed-jobs basis
    instead of the appointment board - both 2026-07-16; v8: canceled-call job
    lists with cancel times so the drill-down dropdowns work on past days -
    2026-07-20) and are recomputed once so the sparklines don't mix definitions.
    """
    cache = _load_json(HISTORY_FILE, {})
    out = {}
    for company, co in COMPANIES.items():
        jt = None
        entries = {e["date"]: e for e in cache.get(company, [])
                   if e.get("sameDayDef") == 7}
        result = []
        for day in _history_days(co["tz"]):
            key = day.isoformat()
            if key not in entries:
                jt = jt or job_type_names(co["tenant"])
                m = compute_day(company, day, jt_names=jt)
                m["date"] = key
                entries[key] = m
                cache[company] = sorted(entries.values(), key=lambda e: e["date"])
                _save_json(HISTORY_FILE, cache)
                if progress:
                    progress(company, key)
            result.append(entries[key])
        out[company] = result
    return out


