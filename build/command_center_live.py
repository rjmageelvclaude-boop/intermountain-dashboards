def compute_day(company, day, jt_names=None):
    """All dashboard metrics for one company and one local calendar day."""
    co = COMPANIES[company]
    tenant = co["tenant"]
    start, end = local_day_window_utc(co["tz"], day)
    jt_names = jt_names or job_type_names(tenant)
    bu = {int(k): v for k, v in co["bu"].items()}
    ropp_tags = set(co["ropp_tags"])
    ropp_removed = set(co.get("ropp_removed_tags") or ())

    def decorate(job):
        job["_bucket"] = bu.get(job.get("businessUnitId"))
        job["_jt_name"] = jt_names.get(job.get("jobTypeId"), "")
        tags = set(job.get("tagTypeIds") or [])
        job["_ropp"] = bool(ropp_tags & tags) and not (ropp_removed & tags)
        return job

    # -- jobs with an appointment today (the "board")
    board = [decorate(j) for j in fetch_all(
        tenant, "/jpm/v2/tenant/{tenant}/jobs",
        {"appointmentStartsOnOrAfter": start, "appointmentStartsBefore": end},
        page_size=500)]
    jobs_by_id = {j["id"]: j for j in board}

    def count(pred):
        return sum(1 for j in board if pred(j))

    live = lambda j: j.get("jobStatus") != "Canceled"
    ran = lambda j: j.get("jobStatus") == "Completed" and _in_window(j.get("completedOn"), start, end)

    m = {}
    m["hvacMaintenanceOnBoard"] = count(lambda j: j["_bucket"] == "hvac_maint" and live(j))
    m["hvacDemandOnBoard"] = count(lambda j: j["_bucket"] == "hvac_demand" and live(j))
    m["hvacJobsOnBoard"] = m["hvacMaintenanceOnBoard"] + m["hvacDemandOnBoard"]
    m["hvacMaintenanceRan"] = count(lambda j: j["_bucket"] == "hvac_maint" and ran(j))
    m["hvacDemandRan"] = count(lambda j: j["_bucket"] == "hvac_demand" and ran(j))
    m["hvacRoppsOnBoard"] = count(lambda j: j["_bucket"] in HVAC_SERVICE and live(j) and j["_ropp"])
    m["hvacRoppsRan"] = count(lambda j: j["_bucket"] in HVAC_SERVICE and ran(j) and j["_ropp"])

    m["plumbMaintenanceOnBoard"] = count(lambda j: j["_bucket"] == "plumb_maint" and live(j))
    m["plumbDemandOnBoard"] = count(lambda j: j["_bucket"] == "plumb_demand" and live(j))
    m["plumbJobsOnBoard"] = m["plumbMaintenanceOnBoard"] + m["plumbDemandOnBoard"]
    m["plumbMaintenanceRan"] = count(lambda j: j["_bucket"] == "plumb_maint" and ran(j))
    m["plumbDemandRan"] = count(lambda j: j["_bucket"] == "plumb_demand" and ran(j))

    # Electrical + Excavation trades (Brothers). Zero everywhere else - the
    # frontend only renders these sections when the company flags them on.
    m["elecMaintenanceOnBoard"] = count(lambda j: j["_bucket"] == "elec_maint" and live(j))
    m["elecDemandOnBoard"] = count(lambda j: j["_bucket"] == "elec_demand" and live(j))
    m["elecJobsOnBoard"] = m["elecMaintenanceOnBoard"] + m["elecDemandOnBoard"]
    m["elecMaintenanceRan"] = count(lambda j: j["_bucket"] == "elec_maint" and ran(j))
    m["elecDemandRan"] = count(lambda j: j["_bucket"] == "elec_demand" and ran(j))
    m["excavOnBoard"] = count(lambda j: j["_bucket"] in EXCAV_ALL and live(j))
    m["excavRan"] = count(lambda j: j["_bucket"] in EXCAV_ALL and ran(j))

    # -- real system installs on today's board, split by trade
    inst = lambda j, t: j["_bucket"] == t and _is_system_install(t, j["_jt_name"])
    m["hvacInstallsOnBoard"] = count(lambda j: live(j) and inst(j, "hvac_install"))
    m["hvacInstallsCompleted"] = count(lambda j: ran(j) and inst(j, "hvac_install"))
    m["plumbInstallsOnBoard"] = count(lambda j: live(j) and inst(j, "plumb_install"))
    m["plumbInstallsCompleted"] = count(lambda j: ran(j) and inst(j, "plumb_install"))
    m["elecInstallsOnBoard"] = count(lambda j: live(j) and inst(j, "elec_install"))
    m["elecInstallsCompleted"] = count(lambda j: ran(j) and inst(j, "elec_install"))

    # Canceled-call drill-downs: per-section job lists in cancel order, feeding
    # the clickable dropdowns and the canceled-estimate alarm overlay. For a
    # Canceled job ServiceTitan stores the cancel timestamp in completedOn
    # (verified against /jobs/{id}/canceled-log - 40/40 jobs across all four
    # tenants, 2026-07-20), so the board pull already has it. Job number/type/
    # time only - no customer data (data.json is public).
    def canceled_rows(pred):
        rows = [{"jobNumber": str(j.get("jobNumber") or j["id"]),
                 "type": j["_jt_name"],
                 "canceledAt": _local_dt_str(j.get("completedOn"), co["tz"])}
                for j in board if j.get("jobStatus") == "Canceled" and pred(j)]
        rows.sort(key=lambda r: r["canceledAt"] or "")
        return rows

    m["hvacSMCanceledJobs"] = canceled_rows(lambda j: j["_bucket"] in HVAC_SERVICE)
    m["hvacSMCanceled"] = len(m["hvacSMCanceledJobs"])
    m["hvacSalesCanceledJobs"] = canceled_rows(lambda j: j["_bucket"] == "hvac_sales")
    m["hvacSalesCanceled"] = len(m["hvacSalesCanceledJobs"])
    m["plumbCanceledJobs"] = canceled_rows(lambda j: (j["_bucket"] or "").startswith("plumb"))
    m["plumbCanceled"] = len(m["plumbCanceledJobs"])
    m["elecCanceledJobs"] = canceled_rows(lambda j: (j["_bucket"] or "").startswith("elec"))
    m["elecCanceled"] = len(m["elecCanceledJobs"])
    m["excavCanceledJobs"] = canceled_rows(lambda j: j["_bucket"] in EXCAV_ALL)
    m["excavCanceled"] = len(m["excavCanceledJobs"])

    # -- jobs created today (feeds TGLs/turnovers and same-day installs)
    created = [decorate(j) for j in fetch_all(
        tenant, "/jpm/v2/tenant/{tenant}/jobs",
        {"createdOnOrAfter": start, "createdBefore": end}, page_size=500)]

    # Turnover-model companies: which sales-BU jobs follow a recent service
    # visit at the same location (one location-batched lookback pull).
    svcgen_ids = svcgen_checked = None
    if co.get("svcgen_sales"):
        seen_sales = {j["id"]: j for j in board + created if j["_bucket"] == "hvac_sales"}
        svcgen_ids = _svcgen_ids(tenant, bu, seen_sales.values(), day, co["tz"])
        svcgen_checked = set(seen_sales)

    # sales-lead board: leads ran today by category
    leads_ran = {"tgl": 0, "costco": 0, "mkt": 0}
    for j in board:
        if j["_bucket"] == "hvac_sales" and ran(j):
            leads_ran[_sales_category(co, j, svcgen_ids)] += 1

    # -- TGLs set today: HVAC Sales jobs created today with a technician on the
    # Lead Generated By field. Matching on job-type name misses leads booked
    # with a plain type (e.g. "Estimate AC"), and ServiceTitan's TGL reports
    # key off Lead Generated By. Canceled jobs still count as set - the tech
    # earned the lead - and are surfaced separately for awareness.
    # A lead only counts as SET once the job that generated it is Completed
    # (RJ 2026-07-15) - while the tech is still on the source job the lead is
    # provisional. Source jobs can be from prior days, so status comes from a
    # by-id pull; a lead with no source jobId has nothing to wait on and
    # counts immediately. Leads whose source job is open today appear on the
    # board as soon as that job completes (15-min refresh), and the 20-day
    # history recompute picks up source jobs that complete on a later day.
    # Turnover-model companies (Brothers) count a sales-BU job created today
    # that follows a service/maintenance visit at the location instead - nobody
    # there sets Lead Generated By (verified ~1-2 jobs/month, 2026-07-13).
    if co.get("svcgen_sales"):
        tgl_created = [j for j in created
                       if j["_bucket"] == "hvac_sales" and j["id"] in svcgen_ids]
    else:
        tgl_candidates = [j for j in created
                          if j["_bucket"] == "hvac_sales"
                          and (j.get("jobGeneratedLeadSource") or {}).get("employeeId")]
        src_ids = sorted({(j["jobGeneratedLeadSource"] or {}).get("jobId")
                          for j in tgl_candidates
                          if (j["jobGeneratedLeadSource"] or {}).get("jobId")})
        src_status = {}
        for i in range(0, len(src_ids), 50):
            for s in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                               {"ids": ",".join(map(str, src_ids[i:i + 50]))}):
                src_status[s["id"]] = s.get("jobStatus")

        def _source_done(j):
            sid = (j["jobGeneratedLeadSource"] or {}).get("jobId")
            return sid is None or src_status.get(sid) == "Completed"

        tgl_created = [j for j in tgl_candidates if _source_done(j)]
    m["tglsSet"] = len(tgl_created)
    m["tglsSetSameDay"] = sum(1 for j in tgl_created if j["id"] in jobs_by_id)
    m["tglsCanceledJobs"] = sorted(
        [{"jobNumber": str(j.get("jobNumber") or j["id"]),
          "type": j["_jt_name"],
          "canceledAt": _local_dt_str(j.get("completedOn"), co["tz"])}
         for j in tgl_created if j.get("jobStatus") == "Canceled"],
        key=lambda r: r["canceledAt"] or "")
    m["tglsCanceled"] = len(m["tglsCanceledJobs"])

    # -- estimates sold today
    estimates = fetch_all(tenant, "/sales/v2/tenant/{tenant}/estimates",
                          {"soldAfter": start, "soldBefore": end}, page_size=500)
    missing = sorted({e["jobId"] for e in estimates if e.get("jobId") and e["jobId"] not in jobs_by_id})
    for i in range(0, len(missing), 50):
        for j in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                           {"ids": ",".join(map(str, missing[i:i + 50]))}):
            jobs_by_id[j["id"]] = decorate(j)

    # -- same-day installs: SOLD today and installing today (RJ 2026-07-15;
    # the old created-today-only test mislabeled late-evening sales whose
    # install job was booked after midnight). Sold today = an estimate sold
    # today on the install's project - the estimate lives on the sales/service
    # job, so the project is the link; Brothers doesn't put installs on
    # projects, so it falls back to a same-location match. The job must also
    # be CREATED today so an add-on estimate sold onto last week's project
    # doesn't relabel an already-booked install.
    sold_projects, sold_locations = set(), set()
    for e in estimates:
        ej = jobs_by_id.get(e.get("jobId"))
        if ej:
            if ej.get("projectId"):
                sold_projects.add(ej["projectId"])
            if ej.get("locationId"):
                sold_locations.add(ej["locationId"])
    created_ids = {j["id"] for j in created}
    same_day = lambda j: (j["id"] in created_ids
                          and (j["projectId"] in sold_projects if j.get("projectId")
                               else j.get("locationId") in sold_locations))
    m["hvacInstallsSameDay"] = count(
        lambda j: live(j) and inst(j, "hvac_install") and same_day(j))
    m["plumbInstallsSameDay"] = count(
        lambda j: live(j) and inst(j, "plumb_install") and same_day(j))
    m["elecInstallsSameDay"] = count(
        lambda j: live(j) and inst(j, "elec_install") and same_day(j))
    m["sameDayDef"] = 8  # history recompute marker - v8: canceled-call job
    # lists with cancel times for the dropdown drill-downs (2026-07-20)

    # an estimate can be sold today on an older sales job that never touched
    # today's board - classify those turnover-or-marketed too
    if svcgen_ids is not None:
        extra = [j for j in jobs_by_id.values()
                 if j["_bucket"] == "hvac_sales" and j["id"] not in svcgen_checked]
        svcgen_ids |= _svcgen_ids(tenant, bu, extra, day, co["tz"])

    daily_sales = hvac_service_sales = plumb_sales = elec_sales = excav_sales = 0.0
    cat_sales = {"tgl": 0.0, "costco": 0.0, "mkt": 0.0}
    cat_sold_jobs = {"tgl": set(), "costco": set(), "mkt": set()}
    for e in estimates:
        amt = float(e.get("subtotal") or 0)
        daily_sales += amt
        job = jobs_by_id.get(e.get("jobId"))
        bucket = job["_bucket"] if job else None
        if bucket in HVAC_SERVICE:
            hvac_service_sales += amt
        elif bucket in PLUMB_ALL:
            plumb_sales += amt
        elif bucket in ELEC_ALL:
            elec_sales += amt
        elif bucket in EXCAV_ALL:
            excav_sales += amt
        elif bucket == "hvac_sales":
            cat = _sales_category(co, job, svcgen_ids)
            cat_sales[cat] += amt
            cat_sold_jobs[cat].add(job["id"])

    # plumbing big-ticket units: by price-book code on today's SOLD estimates
    # where configured (Sierra); otherwise completed install-type jobs.
    wh = {"tankless": 0.0, "filtration": 0.0, "tanks": 0.0}
    wh_skus = co.get("wh_skus")
    wh_patterns = co.get("wh_sku_patterns")
    if wh_skus:
        sku_cat = {code: c for c, codes in wh_skus.items() for code in codes}
        for e in estimates:
            for it in (e.get("items") or []):
                c = sku_cat.get(((it.get("sku") or {}).get("name") or "").strip())
                if c:
                    wh[c] += float(it.get("qty") or 1)
    elif wh_patterns:
        for e in estimates:
            for it in (e.get("items") or []):
                name = ((it.get("sku") or {}).get("name") or "").strip()
                for c, rx in wh_patterns.items():
                    if rx.match(name):
                        wh[c] += float(it.get("qty") or 1)
                        break
    else:
        for j in board:
            if ran(j):
                cat = _wh_category(j["_jt_name"])
                if cat:
                    wh[cat] += 1
    m["plumbTanklessSold"] = int(round(wh["tankless"]))
    m["plumbFiltrationSold"] = int(round(wh["filtration"]))
    m["plumbTanksSold"] = int(round(wh["tanks"]))

    m["dailySales"] = round(daily_sales, 2)
    m["hvacServiceSales"] = round(hvac_service_sales, 2)
    m["plumbSales"] = round(plumb_sales, 2)
    m["elecSales"] = round(elec_sales, 2)
    m["excavSales"] = round(excav_sales, 2)
    for cat, label in (("tgl", "tgl"), ("mkt", "mkt"), ("costco", "costco")):
        total, ran_n, sold_n = cat_sales[cat], leads_ran[cat], len(cat_sold_jobs[cat])
        prefix = label
        m[prefix + "TotalSales"] = round(total, 2)
        m[prefix + "LeadsRan"] = ran_n
        m[prefix + "LeadsSold"] = sold_n
        m[prefix + "Conversion"] = round(sold_n / ran_n * 100, 1) if ran_n else 0
        m[prefix + "AvgTicket"] = round(total / sold_n) if sold_n else 0

    # -- daily revenue: ST-dashboard "Completed Revenue" basis (RJ 2026-07-16) -
    # invoice subtotals of jobs COMPLETED this local day, whatever date the
    # office put on the invoice. The old invoice-date basis drifted from the
    # ST dashboard whenever a job completed late evening was invoiced with the
    # next day's date, and it swept in no-job membership billing invoices.
    # Invoices come from one date-window pull (completion day +/- 1 covers
    # nearly all - invoiceDate is date-only, stored as midnight UTC) with a
    # per-job fallback for jobs whose invoice isn't in the window. Known
    # limit: an adjustment invoice dated 2+ days after completion is missed
    # for jobs whose main invoice WAS found - ST restates the old day, we
    # don't. Accepted; late refunds/adjustments are rare on service tickets.
    completed_jobs = [decorate(j) for j in fetch_all(
        tenant, "/jpm/v2/tenant/{tenant}/jobs",
        {"completedOnOrAfter": start, "completedBefore": end}, page_size=500)
        if j.get("jobStatus") == "Completed"]  # the filter also returns jobs
    # completed then reopened/canceled - ST's dashboard doesn't count those
    day_s = (day - dt.timedelta(days=1)).strftime("%Y-%m-%dT00:00:00Z")
    day_e = (day + dt.timedelta(days=2)).strftime("%Y-%m-%dT00:00:00Z")
    inv_by_job = {}
    for inv in fetch_all(tenant, "/accounting/v2/tenant/{tenant}/invoices",
                         {"invoicedOnOrAfter": day_s, "invoicedOnBefore": day_e},
                         page_size=500):
        jid = (inv.get("job") or {}).get("id")
        if jid:
            inv_by_job.setdefault(jid, []).append(inv)
    for j in completed_jobs:  # invoice dated outside the window (rare)
        if j["id"] not in inv_by_job:
            inv_by_job[j["id"]] = fetch_all(
                tenant, "/accounting/v2/tenant/{tenant}/invoices",
                {"jobId": j["id"]}, page_size=50)
    daily_rev = hvac_service_rev = plumb_rev = elec_rev = excav_rev = 0.0
    memb_inv_jobs = set()  # completed jobs whose invoice carries a membership line
    for j in completed_jobs:
        for inv in inv_by_job.get(j["id"], ()):
            sub = float(inv.get("subTotal") or 0)
            daily_rev += sub
            bucket = bu.get((inv.get("businessUnit") or {}).get("id"))
            if bucket in HVAC_SERVICE:
                hvac_service_rev += sub
            elif bucket in PLUMB_ALL:
                plumb_rev += sub
            elif bucket in ELEC_ALL:
                elec_rev += sub
            elif bucket in EXCAV_ALL:
                excav_rev += sub
            if any((it.get("membershipTypeId") or 0) or it.get("type") == "Membership"
                   for it in (inv.get("items") or [])):
                memb_inv_jobs.add(j["id"])
    m["dailyRevenue"] = round(daily_rev, 2)
    m["hvacServiceRevenue"] = round(hvac_service_rev, 2)
    m["plumbRevenue"] = round(plumb_rev, 2)
    m["elecRevenue"] = round(elec_rev, 2)
    m["excavRevenue"] = round(excav_rev, 2)

    # -- memberships sold today, straight from the memberships endpoint
    # (RJ 2026-07-15: the invoice-line count undercounted - wizard sales often
    # carry no job and their invoice can be dated off-day; audit found 17
    # invoices vs 22 memberships actually created). A membership counts as
    # sold ON A JOB when its customer had a live (non-canceled) job on today's
    # board - the visit the sale happened on - even if the office processed
    # the paperwork. Everything else is an office/phone sale, shown in its own
    # Call Center section. Trade split by the membership's business unit.
    board_custs = {j["customerId"] for j in board
                   if j.get("customerId") and live(j)}
    memb = {"hvac": 0, "plumb": 0, "elec": 0}
    office = {"hvac": 0, "plumb": 0, "elec": 0}
    memb_custs_today = set()  # every customer who bought a membership today
    for mb in fetch_all(tenant, "/memberships/v2/tenant/{tenant}/memberships",
                        {"createdOnOrAfter": start, "createdBefore": end},
                        page_size=200):
        pre = bu.get(mb.get("businessUnitId")) or ""
        trade = ("plumb" if pre.startswith("plumb")
                 else "elec" if pre.startswith(("elec", "excav")) else "hvac")
        cid = mb.get("customerId")
        if cid:
            memb_custs_today.add(cid)
        if cid in board_custs:
            memb[trade] += 1
        else:
            office[trade] += 1
    m["hvacMembershipsSold"] = memb["hvac"]
    m["plumbMembershipsSold"] = memb["plumb"]
    m["elecMembershipsSold"] = memb["elec"]
    m["hvacOfficeMembershipsSold"] = office["hvac"]
    m["plumbOfficeMembershipsSold"] = office["plumb"]
    m["elecOfficeMembershipsSold"] = office["elec"]
    m["officeMembershipsSold"] = sum(office.values())

    # -- options per opportunity + membership conversion / offer rate for the
    # two service trades (RJ 2026-07-14). Jobs COMPLETED this local day,
    # whatever day the appointment was booked (RJ 2026-07-16: switched from
    # the appointment board to match ST's completion-date basis - an
    # after-midnight completion belongs to the day it completed, same call as
    # daily revenue). Opportunity = the call-board formula
    # (not noCharge, or job total >= the job type's soldThreshold). Options =
    # estimates CREATED today on opportunity jobs completed today (a dismissed
    # estimate was still presented). Non-member = the job's customer had no
    # membership active before today (one starting today was sold on this
    # visit, so the job stays in the denominator - tech-board definition).
    # Conversion = non-member jobs where a membership was SOLD on the job
    # (a membership was created today for the job's customer, today's invoice
    # carries a membership line, or a membership SKU is on an estimate sold
    # today - the first signal catches wizard sales with no job attached);
    # offer rate adds jobs where any estimate created
    # today carries a membership SKU. Imports the tech-board helpers at call
    # time (tech_board_live imports this module, so top-level would be circular)
    # to keep SKU patterns and thresholds in one place.
    from tech_board_live import (DEFAULT_SOLD_THRESHOLD, sold_thresholds,
                                 _memberships_for_customers, _member_before,
                                 _is_memb_sku)
    thresholds = sold_thresholds(tenant)
    ests_on_job, sold_on_job = {}, {}
    for e in fetch_all(tenant, "/sales/v2/tenant/{tenant}/estimates",
                       {"createdOnOrAfter": start, "createdBefore": end},
                       page_size=500):
        if e.get("jobId"):
            ests_on_job.setdefault(e["jobId"], []).append(e)
    for e in estimates:
        if e.get("jobId"):
            sold_on_job.setdefault(e["jobId"], []).append(e)

    def memb_on(e):
        return any(_is_memb_sku(company, (it.get("sku") or {}).get("name"))
                   for it in (e.get("items") or []))

    svc_done = {t: [j for j in completed_jobs if j["_bucket"] in bks]
                for t, bks in (("hvac", HVAC_SERVICE), ("plumb", PLUMB_SERVICE))}
    memberships = _memberships_for_customers(
        tenant, {j["customerId"] for js in svc_done.values() for j in js
                 if j.get("customerId")})
    day_iso = day.isoformat()
    for trade, js in svc_done.items():
        opps = options = non_member = nm_sold = nm_off = 0
        for j in js:
            total = float(j.get("total") or 0)
            thr = thresholds.get(j.get("jobTypeId"), DEFAULT_SOLD_THRESHOLD)
            if (not j.get("noCharge")) or total >= thr:
                opps += 1
                options += len(ests_on_job.get(j["id"], ()))
            cid = j.get("customerId")
            if cid is not None and not _member_before(memberships, cid, day_iso):
                non_member += 1
                sold_m = (cid in memb_custs_today
                          or j["id"] in memb_inv_jobs
                          or any(memb_on(e) for e in sold_on_job.get(j["id"], ())))
                nm_sold += sold_m
                nm_off += sold_m or any(memb_on(e) for e in ests_on_job.get(j["id"], ()))
        m[trade + "SvcOpps"] = opps
        m[trade + "OptionsPresented"] = options
        m[trade + "OptionsPerOpp"] = round(options / opps, 2) if opps else 0
        m[trade + "NonMemberJobs"] = non_member
        m[trade + "MembConversion"] = round(nm_sold / non_member * 100, 1) if non_member else 0
        m[trade + "MembOfferRate"] = round(nm_off / non_member * 100, 1) if non_member else 0

    # -- phones
    calls = fetch_all(tenant, "/telecom/v2/tenant/{tenant}/calls",
                      {"createdOnOrAfter": start, "createdBefore": end}, page_size=500)
    inbound = booked = unbooked = 0
    for c in calls:
        lc = c.get("leadCall") or {}
        if lc.get("direction") != "Inbound":
            continue
        inbound += 1
        ct = lc.get("callType")
        if ct == "Booked":
            booked += 1
        elif ct in ("Unbooked", "NotBooked"):
            unbooked += 1
    leads = booked + unbooked
    m["callsInbound"] = inbound
    m["inboundLeads"] = leads
    m["bookingRate"] = round(booked / leads * 100, 1) if leads else 0

    m["flipRate"] = round(m["tglsSet"] / m["hvacRoppsRan"] * 100, 1) if m["hvacRoppsRan"] else 0
    return m


def compute_mtd(company):
    """MTD revenue and sales: 1st of the month through YESTERDAY (tenant-local).

    Today is deliberately excluded - the dashboard pairs these with today's live
    numbers, and pacing runs on budgeted days completed (yesterday inclusive).
    Recomputed from scratch every run so late/retroactive postings are picked up.

    Also emits mtdDailyRevenue - one {date, revenue} per calendar day (zero-filled,
    weekends included) from the same invoice pull - for the MTD Trends chart.
    """
    co = COMPANIES[company]
    tenant = co["tenant"]
    today = local_today(co["tz"])
    if today.day == 1:
        return {"mtdRevenue": 0.0, "mtdSales": 0.0, "mtdThrough": None,
                "mtdDailyRevenue": []}
    first = today.replace(day=1)
    yesterday = today - dt.timedelta(days=1)

    # invoiceDate is a calendar date stored as midnight UTC - no tz shift
    invoices = fetch_all(tenant, "/accounting/v2/tenant/{tenant}/invoices",
                         {"invoicedOnOrAfter": first.strftime("%Y-%m-%dT00:00:00Z"),
                          "invoicedOnBefore": today.strftime("%Y-%m-%dT00:00:00Z")},
                         page_size=500, max_pages=100)
    mtd_rev = sum(float(inv.get("subTotal") or 0) for inv in invoices)

    by_day = {}
    for inv in invoices:
        d = (inv.get("invoiceDate") or "")[:10]
        if d:
            by_day[d] = by_day.get(d, 0.0) + float(inv.get("subTotal") or 0)
    daily = [{"date": (first + dt.timedelta(days=i)).isoformat(),
              "revenue": round(by_day.get((first + dt.timedelta(days=i)).isoformat(), 0.0), 2)}
             for i in range((yesterday - first).days + 1)]

    # estimates sold within the local-time window covering the 1st .. yesterday
    start, _ = local_day_window_utc(co["tz"], first)
    _, end = local_day_window_utc(co["tz"], yesterday)
    estimates = fetch_all(tenant, "/sales/v2/tenant/{tenant}/estimates",
                          {"soldAfter": start, "soldBefore": end},
                          page_size=500, max_pages=100)
    mtd_sales = sum(float(e.get("subtotal") or 0) for e in estimates)

    return {"mtdRevenue": round(mtd_rev, 2), "mtdSales": round(mtd_sales, 2),
            "mtdThrough": yesterday.isoformat(), "mtdDailyRevenue": daily}


def _local_date_str(ts, tz):
    """UTC API timestamp -> tenant-local YYYY-MM-DD (None-safe)."""
    if not ts:
        return None
    t = dt.datetime.strptime(ts[:19], "%Y-%m-%dT%H:%M:%S")
    return (t + dt.timedelta(hours=_utc_offset_hours(tz, t.date()))).date().isoformat()


def _local_dt_str(ts, tz):
    """Tenant-local timestamp 'YYYY-MM-DDTHH:MM' from a ServiceTitan UTC timestamp."""
    if not ts:
        return None
    t = dt.datetime.strptime(ts[:19], "%Y-%m-%dT%H:%M:%S")
    return (t + dt.timedelta(hours=_utc_offset_hours(tz, t.date()))).strftime("%Y-%m-%dT%H:%M")


def compute_install_callbacks(company):
    """Install callbacks ON TODAY'S BOARD (RJ 2026-07-12: only today's returns).

    Anchor installs = install jobs in any mapped BU except HVAC Sales
    (_is_install_anchor) completed in the last CALLBACK_LOOKBACK_DAYS - the
    service departments run installs and callbacks too. A return trip = any job
    in the SAME TRADE (RJ 2026-07-14: HVAC calls only match HVAC installs,
    plumbing calls only plumbing installs - bucket prefix via _trade) at the
    same location created AFTER the install completed - excluding expected
    follow-ups (_CALLBACK_EXEMPT: drywall/QA/permit/drive-by/startup; finish
    jobs like Plumbing Finish Job / Retro Finish DO count), canceled
    jobs, sales-BU estimate visits (a recall booked in the sales BU still
    counts), and other installs (a new purchase isn't a callback).
    Listed rows are return trips with an appointment TODAY, sorted by when the
    callback job was CREATED (oldest first - the frontend numbers them so the
    team can spot a new arrival); `trips` = total times back since the install
    (today included); >2 = CODE PINK (the board blinks). Current-state only -
    not part of per-day history.
    """
    co = COMPANIES[company]
    tenant = co["tenant"]
    tz = co["tz"]
    today = local_today(tz)
    win_start, _ = local_day_window_utc(tz, today - dt.timedelta(days=CALLBACK_LOOKBACK_DAYS))
    day_start, day_end = local_day_window_utc(tz, today)
    jt_names = job_type_names(tenant)
    bu = {int(k): v for k, v in co["bu"].items()}
    jtname = lambda j: jt_names.get(j.get("jobTypeId"), "")
    bucket = lambda j: bu.get(j.get("businessUnitId"))

    installs_by_loc = {}
    for j in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                       {"completedOnOrAfter": win_start, "completedBefore": day_end},
                       page_size=500, max_pages=100):
        if (j.get("jobStatus") == "Completed" and j.get("locationId")
                and _is_install_anchor(bucket(j), jtname(j))):
            installs_by_loc.setdefault(j["locationId"], []).append(j)

    def is_trip(j, install):
        name = jtname(j)
        return (j["id"] != install["id"] and j.get("jobStatus") != "Canceled"
                and (j.get("createdOn") or "") > (install.get("completedOn") or "")
                and _trade(bucket(j)) == _trade(bucket(install))  # HVAC calls <-> HVAC installs only
                and not _is_install_anchor(bucket(j), name)  # a new purchase isn't a callback
                # sales-BU estimate visits don't count, but a recall booked there does
                and (not (bucket(j) or "").endswith("_sales")
                     or re.search(r"recall|warranty", name, re.I))
                and not _CALLBACK_EXEMPT.search(name))

    # Every return trip was created after its install completed, and installs
    # only reach back CALLBACK_LOOKBACK_DAYS, so one created-window pull covers
    # the full trip history of every anchor install.
    created_by_loc = {}
    for j in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                       {"createdOnOrAfter": win_start, "createdBefore": day_end},
                       page_size=500, max_pages=100):
        created_by_loc.setdefault(j.get("locationId"), []).append(j)

    board = fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                      {"appointmentStartsOnOrAfter": day_start,
                       "appointmentStartsBefore": day_end}, page_size=500)
    rows, seen = [], set()
    for j in board:
        if j["id"] in seen or not j.get("locationId"):
            continue
        seen.add(j["id"])
        candidates = [i for i in installs_by_loc.get(j["locationId"], [])
                      if (j.get("createdOn") or "") > (i.get("completedOn") or "")
                      and _trade(bucket(i)) == _trade(bucket(j))]
        if not candidates:
            continue
        install = max(candidates, key=lambda i: i.get("completedOn") or "")
        if not is_trip(j, install):
            continue
        trips = sorted((t for t in created_by_loc.get(j["locationId"], [])
                        if is_trip(t, install)),
                       key=lambda t: t.get("createdOn") or "")
        rows.append({"jobNumber": str(j.get("jobNumber") or j["id"]),
                     "type": jtname(j),
                     "status": j.get("jobStatus"),
                     "createdOn": _local_dt_str(j.get("createdOn"), tz),
                     "installJobNumber": str(install.get("jobNumber") or install["id"]),
                     "installType": jtname(install),
                     "installedOn": _local_date_str(install.get("completedOn"), tz),
                     "trips": len(trips),
                     "tripLog": [{"jobNumber": str(t.get("jobNumber") or t["id"]),
                                  "type": jtname(t),
                                  "day": _local_date_str(t.get("createdOn"), tz)}
                                 for t in trips],
                     "codePink": len(trips) > 2})
    # arrival order (oldest first) - the frontend numbers rows 1..N so the crew
    # can tell at a glance when a NEW callback lands (it appends at the bottom)
    rows.sort(key=lambda r: r["createdOn"] or "")
    return {"installCallbacksToday": rows,
            "installCallbackAlerts": sum(1 for r in rows if r["codePink"]),
            "installCallbackWindowDays": CALLBACK_LOOKBACK_DAYS}


