# ---------------------------------------------------------------- timezones
def _us_dst_bounds(year):
    """(start, end) of US daylight saving time: 2nd Sunday of March 2am -> 1st Sunday of Nov 2am."""
    def nth_sunday(month, n):
        d = dt.date(year, month, 1)
        d += dt.timedelta(days=(6 - d.weekday()) % 7)  # first Sunday
        return d + dt.timedelta(weeks=n - 1)
    return nth_sunday(3, 2), nth_sunday(11, 1)

def _utc_offset_hours(tz, day):
    dst_start, dst_end = _us_dst_bounds(day.year)
    dst = dst_start <= day < dst_end
    if tz == "pacific":
        return -7 if dst else -8
    if tz == "mountain":
        return -6 if dst else -7
    if tz == "arizona":
        return -7
    raise ValueError(tz)

def local_day_window_utc(tz, day):
    """UTC [start, end) covering the tenant-local calendar day."""
    off = _utc_offset_hours(tz, day)
    start = dt.datetime(day.year, day.month, day.day) - dt.timedelta(hours=off)
    end = start + dt.timedelta(days=1)
    fmt = "%Y-%m-%dT%H:%M:%SZ"
    return start.strftime(fmt), end.strftime(fmt)

def local_today(tz):
    utc_now = dt.datetime.utcnow()
    return (utc_now + dt.timedelta(hours=_utc_offset_hours(tz, utc_now.date()))).date()


# ---------------------------------------------------------------- API paging
# Pages fetched concurrently per fetch_all call once page 1 confirms there is
# more; the client's process-wide semaphore keeps total in-flight requests sane.
PAGE_CONCURRENCY = max(1, int(os.environ.get("ST_PAGE_CONCURRENCY", "4")))

def fetch_all(tenant, path, params, page_size=200, max_pages=40, retries=3):
    def get_page(page):
        p = dict(params, pageSize=page_size, page=page)
        last_err = None
        for attempt in range(retries):
            try:
                return st_get(tenant, path, params=p)
            except Exception as e:
                last_err = e
                time.sleep(2 * (attempt + 1))
        raise RuntimeError(f"{path} page {page} failed after {retries} tries: {last_err}")

    first = get_page(1)
    items = list(first.get("data") or [])
    if not first.get("hasMore") or max_pages < 2:
        return items
    # Concurrent waves; a page past the end just comes back empty/hasMore=False.
    next_page, done = 2, False
    with ThreadPoolExecutor(max_workers=PAGE_CONCURRENCY) as pool:
        while not done and next_page <= max_pages:
            wave = range(next_page, min(next_page + PAGE_CONCURRENCY, max_pages + 1))
            for r in pool.map(get_page, wave):
                items.extend(r.get("data") or [])
                if not r.get("hasMore"):
                    done = True
            next_page = wave[-1] + 1
    if not done:
        # Data outgrew the ceiling - numbers computed from this pull are low.
        print(f"WARNING: {path} truncated at {max_pages} pages of {page_size} "
              f"({len(items)} items, hasMore still true) - raise max_pages", flush=True)
    return items


# ------------------------------------------------- parallel company helpers
_history_lock = threading.Lock()

def update_history(path, company, key, rec):
    """Write one closed-month record into a {company: {"YYYY-MM": rec}}
    history file. Reload-merge-save under a lock so parallel company threads
    (and the unlucky-timing case of two of them backfilling at once) can't
    clobber each other's entries. Months older than last year are pruned -
    the boards only ever read the current year, so old keys are dead weight
    that would otherwise ride the Actions cache forever."""
    cutoff = f"{dt.date.today().year - 1:04d}-01"
    with _history_lock:
        cache = _load_json(path, {})
        cache.setdefault(company, {})[key] = rec
        for months in cache.values():
            if isinstance(months, dict):
                for k in [k for k in months
                          if isinstance(k, str) and len(k) == 7 and k < cutoff]:
                    del months[k]
        _save_json(path, cache)

def map_companies(fn, companies):
    """{company: fn(company)} computed in parallel threads, order preserved."""
    companies = list(companies)
    with ThreadPoolExecutor(max_workers=len(companies)) as pool:
        return dict(zip(companies, pool.map(fn, companies)))


# ---------------------------------------------------------------- config cache
def _load_json(path, default):
    try:
        with open(path, encoding="utf-8") as f:
            return json.load(f)
    except (OSError, json.JSONDecodeError):
        return default

def _save_json(path, obj):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    # Unique tmp name: config caches are shared by parallel threads/processes.
    tmp = f"{path}.{os.getpid()}-{threading.get_ident()}.tmp"
    with open(tmp, "w", encoding="utf-8") as f:
        json.dump(obj, f)
    os.replace(tmp, path)

def _cached_names(tenant, kind, path, page_size=100):
    """{id: name} lookup cached on disk (these rarely change)."""
    cache = _load_json(CONFIG_CACHE, {})
    entry = cache.get(f"{tenant}:{kind}")
    if entry and time.time() - entry.get("at", 0) < CONFIG_TTL_HOURS * 3600:
        return {int(k): v for k, v in entry["types"].items()}
    types = {str(j["id"]): (j.get("name") or "") for j in
             fetch_all(tenant, path, {}, page_size=page_size)}
    cache[f"{tenant}:{kind}"] = {"at": time.time(), "types": types}
    _save_json(CONFIG_CACHE, cache)
    return {int(k): v for k, v in types.items()}

def job_type_names(tenant):
    return _cached_names(tenant, "job_types", "/jpm/v2/tenant/{tenant}/job-types")

def membership_type_names(tenant):
    return _cached_names(tenant, "membership_types", "/memberships/v2/tenant/{tenant}/membership-types")


# ---------------------------------------------------------------- classification
def _in_window(ts, start, end):
    return ts is not None and start <= ts < end

def _sales_category(co, job):
    """tgl / costco / mkt for an HVAC-Sales job."""
    name = job["_jt_name"].lower()
    if "tgl" in name:
        return "tgl"
    if job.get("businessUnitId") in co["costco_bu"] or "costco" in name:
        return "costco"
    return "mkt"

def _wh_category(name):
    n = name.lower()
    if "install" not in n:
        return None
    if "tankless" in n:
        return "tankless"
    if "softener" in n or "filtration" in n or "reverse osmosis" in n:
        return "filtration"
    if "tanked water heater" in n or "temp water heater" in n:
        return "tanks"
    return None


# ---------------------------------------------------------------- metric core
def compute_day(company, day, jt_names=None):
    """All dashboard metrics for one company and one local calendar day."""
    co = COMPANIES[company]
    tenant = co["tenant"]
    start, end = local_day_window_utc(co["tz"], day)
    jt_names = jt_names or job_type_names(tenant)
    bu = {int(k): v for k, v in co["bu"].items()}
    ropp_tags = set(co["ropp_tags"])

    def decorate(job):
        job["_bucket"] = bu.get(job.get("businessUnitId"))
        job["_jt_name"] = jt_names.get(job.get("jobTypeId"), "")
        job["_ropp"] = bool(ropp_tags & set(job.get("tagTypeIds") or []))
        return job

    # -- jobs with an appointment today (the "board")
    board = [decorate(j) for j in fetch_all(
        tenant, "/jpm/v2/tenant/{tenant}/jobs",
        {"appointmentStartsOnOrAfter": start, "appointmentStartsBefore": end},
        page_size=500)]
    jobs_by_id = {j["id"]: j for j in board}

    def count(pred):
        return sum(1 for j in board if pred(j))

    live = lambda j: j.get("jobStatus") != "Canceled"
    ran = lambda j: j.get("jobStatus") == "Completed" and _in_window(j.get("completedOn"), start, end)

    m = {}
    m["hvacMaintenanceOnBoard"] = count(lambda j: j["_bucket"] == "hvac_maint" and live(j))
    m["hvacDemandOnBoard"] = count(lambda j: j["_bucket"] == "hvac_demand" and live(j))
    m["hvacJobsOnBoard"] = m["hvacMaintenanceOnBoard"] + m["hvacDemandOnBoard"]
    m["hvacMaintenanceRan"] = count(lambda j: j["_bucket"] == "hvac_maint" and ran(j))
    m["hvacDemandRan"] = count(lambda j: j["_bucket"] == "hvac_demand" and ran(j))
    m["hvacRoppsOnBoard"] = count(lambda j: j["_bucket"] in HVAC_SERVICE and live(j) and j["_ropp"])
    m["hvacRoppsRan"] = count(lambda j: j["_bucket"] in HVAC_SERVICE and ran(j) and j["_ropp"])

    m["plumbMaintenanceOnBoard"] = count(lambda j: j["_bucket"] == "plumb_maint" and live(j))
    m["plumbDemandOnBoard"] = count(lambda j: j["_bucket"] == "plumb_demand" and live(j))
    m["plumbJobsOnBoard"] = m["plumbMaintenanceOnBoard"] + m["plumbDemandOnBoard"]
    m["plumbMaintenanceRan"] = count(lambda j: j["_bucket"] == "plumb_maint" and ran(j))
    m["plumbDemandRan"] = count(lambda j: j["_bucket"] == "plumb_demand" and ran(j))

    # -- real system installs on today's board, split by trade
    inst = lambda j, t: j["_bucket"] == t and _is_system_install(t, j["_jt_name"])
    m["hvacInstallsOnBoard"] = count(lambda j: live(j) and inst(j, "hvac_install"))
    m["hvacInstallsCompleted"] = count(lambda j: ran(j) and inst(j, "hvac_install"))
    m["plumbInstallsOnBoard"] = count(lambda j: live(j) and inst(j, "plumb_install"))
    m["plumbInstallsCompleted"] = count(lambda j: ran(j) and inst(j, "plumb_install"))

    m["hvacSMCanceled"] = count(lambda j: j["_bucket"] in HVAC_SERVICE and j.get("jobStatus") == "Canceled")
    m["hvacSalesCanceled"] = count(lambda j: j["_bucket"] == "hvac_sales" and j.get("jobStatus") == "Canceled")
    m["plumbCanceled"] = count(lambda j: (j["_bucket"] or "").startswith("plumb") and j.get("jobStatus") == "Canceled")

    # sales-lead board: leads ran today by category
    leads_ran = {"tgl": 0, "costco": 0, "mkt": 0}
    for j in board:
        if j["_bucket"] == "hvac_sales" and ran(j):
            leads_ran[_sales_category(co, j)] += 1

    # -- TGLs set today: HVAC Sales jobs created today with a technician on the
    # Lead Generated By field. Matching on job-type name misses leads booked
    # with a plain type (e.g. "Estimate AC"), and ServiceTitan's TGL reports
    # key off Lead Generated By. Canceled jobs still count as set - the tech
    # earned the lead - and are surfaced separately for awareness.
    created = [decorate(j) for j in fetch_all(
        tenant, "/jpm/v2/tenant/{tenant}/jobs",
        {"createdOnOrAfter": start, "createdBefore": end}, page_size=500)]
    tgl_created = [j for j in created
                   if j["_bucket"] == "hvac_sales"
                   and (j.get("jobGeneratedLeadSource") or {}).get("employeeId")]
    m["tglsSet"] = len(tgl_created)
    m["tglsSetSameDay"] = sum(1 for j in tgl_created if j["id"] in jobs_by_id)
    m["tglsCanceled"] = sum(1 for j in tgl_created if j.get("jobStatus") == "Canceled")

    # -- same-day installs: an install job on today's board that was also CREATED
    # today. Install jobs are booked when the sale closes, so created-today +
    # installing-today = sold and installed the same day.
    created_ids = {j["id"] for j in created}
    m["hvacInstallsSameDay"] = count(
        lambda j: live(j) and inst(j, "hvac_install") and j["id"] in created_ids)
    m["plumbInstallsSameDay"] = count(
        lambda j: live(j) and inst(j, "plumb_install") and j["id"] in created_ids)

    # -- estimates sold today
    estimates = fetch_all(tenant, "/sales/v2/tenant/{tenant}/estimates",
                          {"soldAfter": start, "soldBefore": end}, page_size=500)
    missing = sorted({e["jobId"] for e in estimates if e.get("jobId") and e["jobId"] not in jobs_by_id})
    for i in range(0, len(missing), 50):
        for j in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                           {"ids": ",".join(map(str, missing[i:i + 50]))}):
            jobs_by_id[j["id"]] = decorate(j)

    daily_sales = hvac_service_sales = plumb_sales = 0.0
    cat_sales = {"tgl": 0.0, "costco": 0.0, "mkt": 0.0}
    cat_sold_jobs = {"tgl": set(), "costco": set(), "mkt": set()}
    for e in estimates:
        amt = float(e.get("subtotal") or 0)
        daily_sales += amt
        job = jobs_by_id.get(e.get("jobId"))
        bucket = job["_bucket"] if job else None
        if bucket in HVAC_SERVICE:
            hvac_service_sales += amt
        elif bucket in PLUMB_ALL:
            plumb_sales += amt
        elif bucket == "hvac_sales":
            cat = _sales_category(co, job)
            cat_sales[cat] += amt
            cat_sold_jobs[cat].add(job["id"])

    # plumbing big-ticket units: by price-book code on today's SOLD estimates
    # where configured (Sierra); otherwise completed install-type jobs.
    wh = {"tankless": 0.0, "filtration": 0.0, "tanks": 0.0}
    wh_skus = co.get("wh_skus")
    if wh_skus:
        sku_cat = {code: c for c, codes in wh_skus.items() for code in codes}
        for e in estimates:
            for it in (e.get("items") or []):
                c = sku_cat.get(((it.get("sku") or {}).get("name") or "").strip())
                if c:
                    wh[c] += float(it.get("qty") or 1)
    else:
        for j in board:
            if ran(j):
                cat = _wh_category(j["_jt_name"])
                if cat:
                    wh[cat] += 1
    m["plumbTanklessSold"] = int(round(wh["tankless"]))
    m["plumbFiltrationSold"] = int(round(wh["filtration"]))
    m["plumbTanksSold"] = int(round(wh["tanks"]))

    m["dailySales"] = round(daily_sales, 2)
    m["hvacServiceSales"] = round(hvac_service_sales, 2)
    m["plumbSales"] = round(plumb_sales, 2)
    for cat, label in (("tgl", "tgl"), ("mkt", "mkt"), ("costco", "costco")):
        total, ran_n, sold_n = cat_sales[cat], leads_ran[cat], len(cat_sold_jobs[cat])
        prefix = label
        m[prefix + "TotalSales"] = round(total, 2)
        m[prefix + "LeadsRan"] = ran_n
        m[prefix + "LeadsSold"] = sold_n
        m[prefix + "Conversion"] = round(sold_n / ran_n * 100, 1) if ran_n else 0
        m[prefix + "AvgTicket"] = round(total / sold_n) if sold_n else 0

    # -- invoices dated today (invoiceDate is a calendar date stored as midnight UTC)
    day_s = day.strftime("%Y-%m-%dT00:00:00Z")
    day_e = (day + dt.timedelta(days=1)).strftime("%Y-%m-%dT00:00:00Z")
    invoices = fetch_all(tenant, "/accounting/v2/tenant/{tenant}/invoices",
                         {"invoicedOnOrAfter": day_s, "invoicedOnBefore": day_e}, page_size=500)
    daily_rev = hvac_service_rev = plumb_rev = 0.0
    memb = {"hvac": 0, "plumb": 0}
    for inv in invoices:
        sub = float(inv.get("subTotal") or 0)
        daily_rev += sub
        bucket = bu.get((inv.get("businessUnit") or {}).get("id"))
        if bucket in HVAC_SERVICE:
            hvac_service_rev += sub
        elif bucket in PLUMB_ALL:
            plumb_rev += sub
        # memberships sold: distinct invoices carrying a membership line item
        # (multi-system memberships bill one item per system - count the sale once)
        if any((it.get("membershipTypeId") or 0) or it.get("type") == "Membership"
               for it in (inv.get("items") or [])):
            memb["plumb" if (bucket or "").startswith("plumb") else "hvac"] += 1
    m["dailyRevenue"] = round(daily_rev, 2)
    m["hvacServiceRevenue"] = round(hvac_service_rev, 2)
    m["plumbRevenue"] = round(plumb_rev, 2)
    m["hvacMembershipsSold"] = memb["hvac"]
    m["plumbMembershipsSold"] = memb["plumb"]

    # -- phones
    calls = fetch_all(tenant, "/telecom/v2/tenant/{tenant}/calls",
                      {"createdOnOrAfter": start, "createdBefore": end}, page_size=500)
    inbound = booked = unbooked = 0
    for c in calls:
        lc = c.get("leadCall") or {}
        if lc.get("direction") != "Inbound":
            continue
        inbound += 1
        ct = lc.get("callType")
        if ct == "Booked":
            booked += 1
        elif ct in ("Unbooked", "NotBooked"):
            unbooked += 1
    leads = booked + unbooked
    m["callsInbound"] = inbound
    m["inboundLeads"] = leads
    m["bookingRate"] = round(booked / leads * 100, 1) if leads else 0

    m["flipRate"] = round(m["tglsSet"] / m["hvacRoppsRan"] * 100, 1) if m["hvacRoppsRan"] else 0
    return m


def compute_mtd(company):
    """MTD revenue and sales: 1st of the month through YESTERDAY (tenant-local).

    Today is deliberately excluded - the dashboard pairs these with today's live
    numbers, and pacing runs on budgeted days completed (yesterday inclusive).
    Recomputed from scratch every run so late/retroactive postings are picked up.
    """
    co = COMPANIES[company]
    tenant = co["tenant"]
    today = local_today(co["tz"])
    if today.day == 1:
        return {"mtdRevenue": 0.0, "mtdSales": 0.0, "mtdThrough": None}
    first = today.replace(day=1)
    yesterday = today - dt.timedelta(days=1)

    # invoiceDate is a calendar date stored as midnight UTC - no tz shift
    invoices = fetch_all(tenant, "/accounting/v2/tenant/{tenant}/invoices",
                         {"invoicedOnOrAfter": first.strftime("%Y-%m-%dT00:00:00Z"),
                          "invoicedOnBefore": today.strftime("%Y-%m-%dT00:00:00Z")},
                         page_size=500, max_pages=100)
    mtd_rev = sum(float(inv.get("subTotal") or 0) for inv in invoices)

    # estimates sold within the local-time window covering the 1st .. yesterday
    start, _ = local_day_window_utc(co["tz"], first)
    _, end = local_day_window_utc(co["tz"], yesterday)
    estimates = fetch_all(tenant, "/sales/v2/tenant/{tenant}/estimates",
                          {"soldAfter": start, "soldBefore": end},
                          page_size=500, max_pages=100)
    mtd_sales = sum(float(e.get("subtotal") or 0) for e in estimates)

    return {"mtdRevenue": round(mtd_rev, 2), "mtdSales": round(mtd_sales, 2),
            "mtdThrough": yesterday.isoformat()}


def _local_date_str(ts, tz):
    """UTC API timestamp -> tenant-local YYYY-MM-DD (None-safe)."""
    if not ts:
        return None
    t = dt.datetime.strptime(ts[:19], "%Y-%m-%dT%H:%M:%S")
    return (t + dt.timedelta(hours=_utc_offset_hours(tz, t.date()))).date().isoformat()


def compute_install_callbacks(company):
    """Install callbacks ON TODAY'S BOARD (RJ 2026-07-12: only today's returns).

    Anchor installs = install jobs in any mapped BU except HVAC Sales
    (_is_install_anchor) completed in the last CALLBACK_LOOKBACK_DAYS - the
    service departments run installs and callbacks too. A return trip = any job
    in any department at the same location created AFTER the install completed -
    excluding expected follow-ups (_CALLBACK_EXEMPT: drywall/QA/permit/finish/
    startup), canceled jobs, sales-BU estimate visits (a recall booked in the
    sales BU still counts), and other installs (a new purchase isn't a callback).
    Listed rows are return trips with an appointment TODAY; `trips` = total
    times back since the install (today included); >2 = CODE PINK (the board
    blinks). Current-state only - not part of per-day history.
    """
    co = COMPANIES[company]
    tenant = co["tenant"]
    tz = co["tz"]
    today = local_today(tz)
    win_start, _ = local_day_window_utc(tz, today - dt.timedelta(days=CALLBACK_LOOKBACK_DAYS))
    day_start, day_end = local_day_window_utc(tz, today)
    jt_names = job_type_names(tenant)
    bu = {int(k): v for k, v in co["bu"].items()}
    jtname = lambda j: jt_names.get(j.get("jobTypeId"), "")
    bucket = lambda j: bu.get(j.get("businessUnitId"))

    installs_by_loc = {}
    for j in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                       {"completedOnOrAfter": win_start, "completedBefore": day_end},
                       page_size=500, max_pages=100):
        if (j.get("jobStatus") == "Completed" and j.get("locationId")
                and _is_install_anchor(bucket(j), jtname(j))):
            installs_by_loc.setdefault(j["locationId"], []).append(j)

    def is_trip(j, install):
        name = jtname(j)
        return (j["id"] != install["id"] and j.get("jobStatus") != "Canceled"
                and (j.get("createdOn") or "") > (install.get("completedOn") or "")
                and not _is_install_anchor(bucket(j), name)  # a new purchase isn't a callback
                # sales-BU estimate visits don't count, but a recall booked there does
                and (bucket(j) != "hvac_sales" or re.search(r"recall|warranty", name, re.I))
                and not _CALLBACK_EXEMPT.search(name))

    # Every return trip was created after its install completed, and installs
    # only reach back CALLBACK_LOOKBACK_DAYS, so one created-window pull covers
    # the full trip history of every anchor install.
    created_by_loc = {}
    for j in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                       {"createdOnOrAfter": win_start, "createdBefore": day_end},
                       page_size=500, max_pages=100):
        created_by_loc.setdefault(j.get("locationId"), []).append(j)

    board = fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                      {"appointmentStartsOnOrAfter": day_start,
                       "appointmentStartsBefore": day_end}, page_size=500)
    rows, seen = [], set()
    for j in board:
        if j["id"] in seen or not j.get("locationId"):
            continue
        seen.add(j["id"])
        candidates = [i for i in installs_by_loc.get(j["locationId"], [])
                      if (j.get("createdOn") or "") > (i.get("completedOn") or "")]
        if not candidates:
            continue
        install = max(candidates, key=lambda i: i.get("completedOn") or "")
        if not is_trip(j, install):
            continue
        trips = sorted((t for t in created_by_loc.get(j["locationId"], [])
                        if is_trip(t, install)),
                       key=lambda t: t.get("createdOn") or "")
        rows.append({"jobNumber": str(j.get("jobNumber") or j["id"]),
                     "type": jtname(j),
                     "status": j.get("jobStatus"),
                     "installJobNumber": str(install.get("jobNumber") or install["id"]),
                     "installType": jtname(install),
                     "installedOn": _local_date_str(install.get("completedOn"), tz),
                     "trips": len(trips),
                     "tripLog": [{"jobNumber": str(t.get("jobNumber") or t["id"]),
                                  "type": jtname(t),
                                  "day": _local_date_str(t.get("createdOn"), tz)}
                                 for t in trips],
                     "codePink": len(trips) > 2})
    rows.sort(key=lambda r: (-r["trips"], r["installedOn"] or ""))
    return {"installCallbacksToday": rows,
            "installCallbackAlerts": sum(1 for r in rows if r["codePink"]),
            "installCallbackWindowDays": CALLBACK_LOOKBACK_DAYS}


