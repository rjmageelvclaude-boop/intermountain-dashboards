#!/usr/bin/env python3
"""
Live ServiceTitan engine for the HVAC Service Tech Leaderboard.

Ranks non-silo HVAC service technicians by total revenue, MTD and YTD, for
Sierra, Ultimate and Russett (plus a combined board). Roster comes from
People > Technicians (active only), picked by team:

    sierra     team starts with "2a" / "2b"
    ultimate   team starts with "1B" / "1C"
    russett    team is "Service-2/Repair" / "Service - Maintenance"

Per tech, per calendar month (summed across months for YTD):

  revenue        job.total of completed jobs, split by the payroll job-split %
                 (splits sum to 100 per job; jobs are counted in the month
                 they were completed, tenant-local)
  jobs / opps    completed jobs where the tech holds a split > 0; opportunity
                 uses the call-board formula: !noCharge or total >= jobType
                 soldThreshold (default $100)
  converted      opportunity jobs with total >= soldThreshold; conv rate =
                 converted / opps
  avg ticket     revenue / opps
  total sales    subtotal of estimates SOLD in the month with soldBy == tech
  memberships    membership line items (see MEMB_SKU per tenant) on sold
                 estimates of the tech's completed jobs - renewals included
  memb conv      non-member jobs where a membership was sold / non-member jobs
                 ("non-member" = the job's customer had no membership active
                 before that day; one started the same day counts as sold on
                 that visit, so the job stays in the denominator)
  memb offered   non-member jobs where ANY estimate on the job (sold or not)
                 carries a membership line item / non-member jobs
  tgl sales      subtotal of estimates sold in the month whose job was
                 generated by this tech (job.jobGeneratedLeadSource.employeeId)

Membership line items by tenant (sku name on the estimate item):
    SIE  SAM01..SAM12
    ULT  names containing "shield" (Lion Shield / Climate Shield programs),
         excluding billing/cancel adjustment items
    RUS  MVP* items ("MVP New Sale", "MVP 2 Free Years", add-ons),
         excluding member-discount price modifiers

Closed months are cached in data/tech-board-history.json and recomputed at
most daily until they are 10 days past month-end, then frozen. The current
month is recomputed on every run.

CLI smoke test:
    py build/tech_board_live.py                 # all companies, current month
    py build/tech_board_live.py sierra 2026-06  # one company, one month
"""
import datetime as dt
import json
import os
import re
import sys
import time

HERE = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(HERE)
sys.path.insert(0, HERE)
from command_center_live import (fetch_all, local_today, _load_json,
                                 _save_json, _utc_offset_hours,
                                 map_companies, update_history)
from servicetitan_client import st_get

HISTORY_FILE = os.path.join(ROOT, "data", "tech-board-history.json")
CONFIG_CACHE = os.path.join(ROOT, "data", "tech-board-st-config.json")
CONFIG_TTL_HOURS = 24 * 7
DEFAULT_SOLD_THRESHOLD = 100.0
SPLITS_BUFFER_DAYS = 60      # splits are created at assignment, before completion
ESTIMATE_BUFFER_DAYS = 30    # estimates on a job can predate the completion month
MONTH_FREEZE_DAYS = 10       # closed month is final this long after month-end
MONTH_RECHECK_HOURS = 24     # until frozen, closed months refresh at most daily

COMPANIES = {
    "sierra": {
        "tenant": "SIE", "tz": "pacific", "label": "Sierra",
        "color": "#1663c7",
        "team_match": lambda t: t.startswith(("2a", "2b")),
    },
    "ultimate": {
        "tenant": "ULT", "tz": "mountain", "label": "Ultimate",
        "color": "#c7161d",
        "team_match": lambda t: t.startswith(("1b", "1c")),
    },
    "russett": {
        "tenant": "RUS", "tz": "arizona", "label": "Russett",
        "color": "#0e7a3d",
        "team_match": lambda t: t in ("service-2/repair", "service - maintenance"),
    },
}

_SAM = re.compile(r"^sam(0[1-9]|1[0-2])$")

def _is_memb_sku(company, name):
    n = (name or "").strip().lower()
    if not n:
        return False
    if company == "sierra":
        return bool(_SAM.match(n))
    if company == "ultimate":
        return "shield" in n and not any(
            w in n for w in ("cancel", "mthly", "billing", "credit", "deferred", "adj"))
    if company == "russett":
        return n.startswith("mvp") and "discount" not in n
    if company == "brothers":
        # Home Care Club: HCC* task codes (HCCAC, HCCPL, HCCWAIVED67, "HCC New
        # Monthly Membership", the "Join the Club!" advertisement task) plus the
        # membership-named add-ons. Discount lines aren't an offer/sale marker.
        return (n.startswith("hcc") or "monthly membership" in n) and "discount" not in n
    return False


# ---------------------------------------------------------------- utilities
def _iso_z(d):
    return d.strftime("%Y-%m-%dT%H:%M:%SZ")

def _local_ts_utc(tz, day, hour=0):
    """UTC timestamp of local midnight (+hours) on `day`."""
    off = _utc_offset_hours(tz, day)
    return dt.datetime(day.year, day.month, day.day) + dt.timedelta(hours=hour - off)

def month_window_utc(tz, year, month):
    """UTC [start, end) covering the tenant-local calendar month."""
    first = dt.date(year, month, 1)
    nxt = dt.date(year + (month == 12), month % 12 + 1, 1)
    return _iso_z(_local_ts_utc(tz, first)), _iso_z(_local_ts_utc(tz, nxt))

def window_utc(tz, day_from, day_to):
    """UTC [start, end) covering tenant-local days day_from..day_to inclusive."""
    return (_iso_z(_local_ts_utc(tz, day_from)),
            _iso_z(_local_ts_utc(tz, day_to + dt.timedelta(days=1))))

def _parse_day(ts):
    """Date part of an API timestamp ('2026-07-01T...Z') - no tz shift."""
    return ts[:10] if ts else None

def _local_completed_day(tz, ts):
    """Tenant-local calendar day a UTC completedOn timestamp falls on."""
    t = dt.datetime.strptime(ts[:19], "%Y-%m-%dT%H:%M:%S")
    return (t + dt.timedelta(hours=_utc_offset_hours(tz, t.date()))).date()

def clean_name(name):
    n = re.sub(r"\s*-\s*tech\s*$", "", (name or "").strip(), flags=re.I)
    return re.sub(r"\s+", " ", n)


# ---------------------------------------------------------------- config
def sold_thresholds(tenant):
    """{jobTypeId: soldThreshold} cached on disk."""
    cache = _load_json(CONFIG_CACHE, {})
    entry = cache.get(f"{tenant}:job_types")
    if entry and time.time() - entry.get("at", 0) < CONFIG_TTL_HOURS * 3600:
        return {int(k): v for k, v in entry["thresholds"].items()}
    thresholds = {}
    for jt in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/job-types", {}, page_size=100):
        thresholds[str(jt["id"])] = float(jt.get("soldThreshold") or DEFAULT_SOLD_THRESHOLD)
    cache[f"{tenant}:job_types"] = {"at": time.time(), "thresholds": thresholds}
    _save_json(CONFIG_CACHE, cache)
    return {int(k): v for k, v in thresholds.items()}


# ---------------------------------------------------------------- roster
def team_technicians(company):
    """Active technicians on the board's teams: {techId: {name, team, ids}}."""
    co = COMPANIES[company]
    techs = fetch_all(co["tenant"], "/settings/v2/tenant/{tenant}/technicians",
                      {"active": "True"}, page_size=200)
    roster = {}
    for t in techs:
        team = re.sub(r"\s+", " ", (t.get("team") or "").strip().lower())
        if team and co["team_match"](team):
            roster[t["id"]] = {
                "id": t["id"],
                "userId": t.get("userId"),
                "name": clean_name(t.get("name")),
                "team": (t.get("team") or "").strip(),
            }
    return roster


# ---------------------------------------------------------------- memberships
def _memberships_for_customers(tenant, customer_ids):
    """{customerId: [(from_day, end_day_or_None)]} for every membership."""
    out = {}
    ids = sorted(customer_ids)
    for i in range(0, len(ids), 100):
        batch = ",".join(map(str, ids[i:i + 100]))
        for m in fetch_all(tenant, "/memberships/v2/tenant/{tenant}/memberships",
                           {"customerIds": batch}, page_size=200):
            start = _parse_day(m.get("from")) or _parse_day(m.get("createdOn"))
            end = _parse_day(m.get("to"))
            cancel = _parse_day(m.get("cancellationDate"))
            if cancel and (not end or cancel < end):
                end = cancel
            if start:
                out.setdefault(m["customerId"], []).append((start, end))
    return out

def _member_before(memberships, customer_id, day_iso):
    """Was the customer already a member before this local day?
    A membership starting the same day was sold on that visit."""
    for start, end in memberships.get(customer_id, ()):
        if start < day_iso and (end is None or end >= day_iso):
            return True
    return False


# ---------------------------------------------------------------- month core
def _new_counters():
    return {"revenue": 0.0, "jobs": 0, "opps": 0, "converted": 0,
            "sales": 0.0, "membSold": 0, "nonMemberJobs": 0,
            "nonMemberSold": 0, "nonMemberOffered": 0, "tglSales": 0.0}

def compute_month(company, year, month, roster=None, thresholds=None):
    """Raw per-tech counters for one company and one calendar month."""
    first = dt.date(year, month, 1)
    last = dt.date(year + (month == 12), month % 12 + 1, 1) - dt.timedelta(days=1)
    return compute_window(company, first, last, roster=roster, thresholds=thresholds)

def compute_window(company, day_from, day_to, roster=None, thresholds=None):
    """Raw per-tech counters for one company over tenant-local days
    day_from..day_to inclusive (the scorecard uses payroll-period windows;
    the leaderboard's months are just full-month windows)."""
    co = COMPANIES[company]
    tenant, tz = co["tenant"], co["tz"]
    roster = roster if roster is not None else team_technicians(company)
    thresholds = thresholds if thresholds is not None else sold_thresholds(tenant)
    start, end = window_utc(tz, day_from, day_to)
    buf_splits = _iso_z(_local_ts_utc(tz, day_from)
                        - dt.timedelta(days=SPLITS_BUFFER_DAYS))
    buf_est = _iso_z(_local_ts_utc(tz, day_from)
                     - dt.timedelta(days=ESTIMATE_BUFFER_DAYS))

    # -- completed jobs in the window
    jobs = fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                     {"completedOnOrAfter": start, "completedBefore": end,
                      "jobStatus": "Completed"}, page_size=500, max_pages=200)
    jobs_by_id = {j["id"]: j for j in jobs}

    # -- payroll splits (tech attribution + revenue %)
    splits_by_job = {}
    for s in fetch_all(tenant, "/payroll/v2/tenant/{tenant}/jobs/splits",
                       {"createdOnOrAfter": buf_splits, "createdBefore": end},
                       page_size=500, max_pages=400):
        splits_by_job.setdefault(s["jobId"], []).append(s)
    missing = [j for j in jobs_by_id if j not in splits_by_job][:300]
    for jid in missing:  # rare (<0.5%): per-job fallback
        try:
            r = st_get(tenant, f"/payroll/v2/tenant/{{tenant}}/jobs/{jid}/splits")
            if r.get("data"):
                splits_by_job[jid] = r["data"]
        except Exception:
            pass

    # -- estimates: created near the month (offered-rate join by job) and
    #    sold inside the month (sales, memberships, TGL)
    est_created = fetch_all(tenant, "/sales/v2/tenant/{tenant}/estimates",
                            {"createdOnOrAfter": buf_est, "createdBefore": end},
                            page_size=500, max_pages=200)
    est_sold = [e for e in fetch_all(
        tenant, "/sales/v2/tenant/{tenant}/estimates",
        {"soldAfter": start, "soldBefore": end}, page_size=500, max_pages=200)
        if ((e.get("status") or {}).get("name") or "") != "Dismissed"]

    def memb_qty(est):
        return sum(max(1, int(float(it.get("qty") or 0)))
                   for it in (est.get("items") or [])
                   if _is_memb_sku(company, (it.get("sku") or {}).get("name")))

    ests_on_job, sold_on_job = {}, {}
    for e in est_created:
        if e.get("jobId"):
            ests_on_job.setdefault(e["jobId"], []).append(e)
    for e in est_sold:
        if e.get("jobId"):
            sold_on_job.setdefault(e["jobId"], []).append(e)

    # -- membership status for customers of the roster's completed jobs
    team_jobs = []
    for j in jobs:
        techs = [s for s in splits_by_job.get(j["id"], ())
                 if s["technicianId"] in roster and (s.get("split") or 0) > 0]
        if techs:
            team_jobs.append((j, techs))
    memberships = _memberships_for_customers(
        tenant, {j["customerId"] for j, _ in team_jobs if j.get("customerId")})

    # -- jobs referenced by sold estimates (for TGL attribution)
    est_job_ids = sorted({e["jobId"] for e in est_sold
                          if e.get("jobId") and e["jobId"] not in jobs_by_id})
    for i in range(0, len(est_job_ids), 50):
        for j in fetch_all(tenant, "/jpm/v2/tenant/{tenant}/jobs",
                           {"ids": ",".join(map(str, est_job_ids[i:i + 50]))}):
            jobs_by_id[j["id"]] = j

    tech_key = {}
    for t in roster.values():
        tech_key[t["id"]] = t["id"]
        if t.get("userId"):
            tech_key[t["userId"]] = t["id"]

    out = {tid: _new_counters() for tid in roster}

    # -- job-based metrics
    for job, techs in team_jobs:
        total = float(job.get("total") or 0)
        thr = thresholds.get(job.get("jobTypeId"), DEFAULT_SOLD_THRESHOLD)
        opp = (not job.get("noCharge")) or total >= thr
        conv = opp and total >= thr
        day = _local_completed_day(tz, job["completedOn"]).isoformat()
        non_member = (job.get("customerId") is not None
                      and not _member_before(memberships, job["customerId"], day))
        offered = any(memb_qty(e) for e in ests_on_job.get(job["id"], ()))
        sold_memb = sum(memb_qty(e) for e in sold_on_job.get(job["id"], ()))
        for s in techs:
            c = out[s["technicianId"]]
            c["revenue"] += total * float(s.get("split") or 0) / 100.0
            c["jobs"] += 1
            c["opps"] += opp
            c["converted"] += conv
            c["membSold"] += sold_memb
            if non_member:
                c["nonMemberJobs"] += 1
                c["nonMemberOffered"] += bool(offered or sold_memb)
                c["nonMemberSold"] += bool(sold_memb)

    # -- estimate-based metrics (sold-by / lead-generated-by)
    for e in est_sold:
        amt = float(e.get("subtotal") or 0)
        seller = tech_key.get(e.get("soldBy"))
        if seller:
            out[seller]["sales"] += amt
        job = jobs_by_id.get(e.get("jobId"))
        src = (job or {}).get("jobGeneratedLeadSource") or {}
        gen = tech_key.get(src.get("employeeId"))
        if gen:
            out[gen]["tglSales"] += amt

    for c in out.values():
        c["revenue"] = round(c["revenue"], 2)
        c["sales"] = round(c["sales"], 2)
        c["tglSales"] = round(c["tglSales"], 2)
    return out


# ---------------------------------------------------------------- caching
def _month_key(year, month):
    return f"{year:04d}-{month:02d}"

def months_of_year(company):
    today = local_today(COMPANIES[company]["tz"])
    return [(today.year, m) for m in range(1, today.month + 1)], today

def compute_company(company, deadline=None, progress=None):
    """Per-tech counters for every month this year, cached for closed months.
    Returns (months_dict, roster, complete)."""
    def _int_keys(techs):
        return {int(k): v for k, v in techs.items()}

    cache = _load_json(HISTORY_FILE, {})
    co_cache = cache.setdefault(company, {})
    months, today = months_of_year(company)
    roster = team_technicians(company)
    thresholds = sold_thresholds(COMPANIES[company]["tenant"])
    current_key = _month_key(today.year, today.month)
    complete = True

    result = {}
    for year, month in months:
        key = _month_key(year, month)
        entry = co_cache.get(key)
        if entry and key != current_key:
            month_end = dt.date(year + (month == 12), month % 12 + 1, 1)
            frozen = (today - month_end).days >= MONTH_FREEZE_DAYS and entry.get("final")
            fresh = time.time() - entry.get("at", 0) < MONTH_RECHECK_HOURS * 3600
            if frozen or fresh:
                result[key] = _int_keys(entry["techs"])
                continue
        if deadline and time.time() > deadline and key != current_key:
            complete = False   # out of time - keep whatever cache we have
            if entry:
                result[key] = _int_keys(entry["techs"])
            continue
        t0 = time.time()
        techs = compute_month(company, year, month, roster=roster, thresholds=thresholds)
        result[key] = techs
        if key != current_key:
            month_end = dt.date(year + (month == 12), month % 12 + 1, 1)
            rec = {"at": time.time(), "techs": techs,
                   "final": (today - month_end).days >= MONTH_FREEZE_DAYS}
            update_history(HISTORY_FILE, company, key, rec)
        if progress:
            progress(company, key, time.time() - t0)
    return result, roster, complete


# ---------------------------------------------------------------- public API
def _finalize(counters):
    c = counters
    return {
        "revenue": round(c["revenue"], 2),
        "sales": round(c["sales"], 2),
        "jobs": c["jobs"],
        "opps": c["opps"],
        "avgTicket": round(c["revenue"] / c["opps"]) if c["opps"] else 0,
        "convRate": round(c["converted"] / c["opps"] * 100, 1) if c["opps"] else 0,
        "membSold": c["membSold"],
        "membConv": round(c["nonMemberSold"] / c["nonMemberJobs"] * 100, 1) if c["nonMemberJobs"] else 0,
        "membOffered": round(c["nonMemberOffered"] / c["nonMemberJobs"] * 100, 1) if c["nonMemberJobs"] else 0,
        "nonMemberJobs": c["nonMemberJobs"],
        "tglSales": round(c["tglSales"], 2),
    }

def _sum_counters(dicts):
    total = _new_counters()
    for d in dicts:
        for k, v in d.items():
            total[k] += v
    return total

def compute(time_budget_secs=None, progress=None):
    """Full data.json payload for the dashboard."""
    deadline = time.time() + time_budget_secs if time_budget_secs else None
    boards = {"mtd": {}, "ytd": {}}
    complete = True
    results = map_companies(
        lambda c: compute_company(c, deadline=deadline, progress=progress), COMPANIES)
    for company, co in COMPANIES.items():
        _, today = months_of_year(company)
        per_month, roster, ok = results[company]
        complete = complete and ok
        current_key = _month_key(today.year, today.month)

        rows_mtd, rows_ytd = [], []
        for tid, info in roster.items():
            base = {"id": tid, "name": info["name"], "team": info["team"],
                    "company": company, "companyLabel": co["label"], "color": co["color"]}
            mtd = per_month.get(current_key, {}).get(tid) or _new_counters()
            ytd = _sum_counters([m.get(tid) or _new_counters() for m in per_month.values()])
            rows_mtd.append(dict(base, **_finalize(mtd)))
            rows_ytd.append(dict(base, **_finalize(ytd)))
        for rows, view in ((rows_mtd, "mtd"), (rows_ytd, "ytd")):
            rows.sort(key=lambda r: -r["revenue"])
            boards[view][company] = rows

    for view in ("mtd", "ytd"):
        combined = [r for c in COMPANIES for r in boards[view][c]]
        combined.sort(key=lambda r: -r["revenue"])
        boards[view]["combined"] = combined

    today = local_today("pacific")
    return {
        "updated": dt.datetime.now().strftime("%a %b %d %Y %H:%M:%S"),
        "complete": complete,
        "period": {"mtd": today.strftime("%B %Y"), "ytd": str(today.year)},
        "companies": {c: {"label": co["label"], "color": co["color"]}
                      for c, co in COMPANIES.items()},
        "boards": boards,
    }


if __name__ == "__main__":
    sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    if len(sys.argv) > 1:
        company = sys.argv[1]
        ym = sys.argv[2] if len(sys.argv) > 2 else None
        if ym:
            y, m = map(int, ym.split("-"))
        else:
            t = local_today(COMPANIES[company]["tz"])
            y, m = t.year, t.month
        t0 = time.time()
        roster = team_technicians(company)
        techs = compute_month(company, y, m)
        rows = sorted(((c["revenue"], roster[tid]["name"], c) for tid, c in techs.items()
                       if tid in roster), reverse=True)
        for rev, name, c in rows:
            print(f"{name:28s} rev {rev:>10,.0f} jobs {c['jobs']:>3} opps {c['opps']:>3} "
                  f"conv {c['converted']:>3} sales {c['sales']:>9,.0f} memb {c['membSold']:>2} "
                  f"nonMem {c['nonMemberJobs']:>3} off {c['nonMemberOffered']:>3} "
                  f"tgl {c['tglSales']:>9,.0f}")
        print(f"-- {company} {y}-{m:02d} in {time.time() - t0:.1f}s", file=sys.stderr)
    else:
        print(json.dumps(compute(), indent=1)[:2000])
